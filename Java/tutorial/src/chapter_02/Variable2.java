package chapter_02;

/**
 * 実行するためのクラス
 *
 * @version 1.0
 * @author naoki
 */
public class Variable2 {
    /**
     * main method
     */
    public static void main(String[] args) {
        int number = 10; // 変数の初期化
        number = number + 1; // numberに1を足す
        System.out.println(number); // numberを表示する
    }

    /*
     * 命名規則 基本的に小文字 クラス名： 先頭文字を大文字に 変数名、パッケージ名: 全て小文字
     */
}
