package chapter_03;

public class EnzansiExec06 {
    public static void main(String[] args) {
        // 加減乗算
        int a = 20;
        int b = 30;
        int a1, a2, a3, a4, a5;

        a1 = a + b;
        a2 = a - b;
        a3 = a * b;
        a4 = a / b;
        a5 = a % b;

        System.out.println("a1 = " + a1);
        System.out.println("a1 = " + a2);
        System.out.println("a1 = " + a3);
        System.out.println("a1 = " + a4);
        System.out.println("a1 = " + a5);
    }
}
