package chapter_03;

public class EnzansiExec07 {
    public static void main(String[] args) {
        /*
         * 変数 = 式;
         */
        int a, b, c;
        a = b = c = 5; // a,b,cに5を代入する

        System.out.println("a= " + a);
        System.out.println("a= " + b);
        System.out.println("a= " + c);
    }
}
